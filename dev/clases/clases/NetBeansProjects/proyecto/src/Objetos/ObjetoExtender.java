package Objetos;

/**
 *
 * @author Home
 */
public class ObjetoExtender {
    private String codigo,nombre;
    private double valor;

    public ObjetoExtender(String codigo, String nombre, double valor) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.valor = valor;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
    
    
    
    
}
